package objetos;

public class Sala_ocupante {
    private Sala sala;
    private Ocupante ocupante;

    /**
     * @return the sala
     */
    public Sala getSala() {
        return sala;
    }

    /**
     * @param sala the sala to set
     */
    public void setSala(Sala sala) {
        this.sala = sala;
    }

    /**
     * @return the ocupante
     */
    public Ocupante getOcupante() {
        return ocupante;
    }

    /**
     * @param ocupante the ocupante to set
     */
    public void setOcupante(Ocupante ocupante) {
        this.ocupante = ocupante;
    }
}